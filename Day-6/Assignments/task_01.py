a = 2
b = 3

def addition(a, b):
    add = a + b
    return(add)

def subtraction(a, b):
    sub = a - b
    return(sub)

def multiplication( a, b):
    mul = a * b
    return(mul)

def division(a , b):
    div = a / b
    return(div)

print(addition(a, b))
print(subtraction(a, b))
print(multiplication(a, b))
print(division(a, b))

