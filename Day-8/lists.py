# this is list example

# List are mutable in nature
my_list = ["student0", "student1", "student2", "student3","student4"]
my_list.append("student5") # adds element to list 
my_list.remove("student1") # removes element from list
print(len(my_list)) # Length of my_list
print(my_list)

# Tuple are immutable in nature
my_tuple = ("student0", "student1", "student2", "student3","student4")
print(my_tuple)
print(len(my_tuple))